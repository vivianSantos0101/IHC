def populate_db(conn, log):
    '''Insere os dados iniciais na tabela equipes_f1.'''
    
    cursor = conn.cursor()
    log.info("População inicial de dados da F1 em andamento...")
    
    # Dados da tabela de equipes (equipes_f1)
    log.info("-> Inserindo dados da tabela 'equipes_f1' (2025)...")
    for equipe in equipes_f1_2025:
        pilotos_str = ", ".join(equipe["pilotos_principais"])
        cursor.execute('''
            INSERT INTO equipes_f1 (nome, nacionalidade, pontos, vitorias, podios, pilotos_principais)
            VALUES (?, ?, ?, ?, ?, ?)
        ''', (equipe["nome"], equipe["nacionalidade"], equipe["pontos"], equipe["vitorias"],  
              equipe["podios"], pilotos_str))
              
    log.info("✅ Dados de Equipes (2025) inseridos com sucesso!")
    
    # Dados da tabela de historico de campeões (historico_campeoes)
    log.info("-> Inserindo dados da tabela 'historico_campeoes' (1960-2024)...")
    for campeao in campeonatos_vencidos:
                    
        cursor.execute('''
            INSERT INTO historico_campeoes (ano_vencido, escuderia, piloto, pontos, construtora_campea)
            VALUES (?, ?, ?, ?, ?)
        ''', (campeao["ano_vencido"], campeao["escuderia"], campeao["piloto"], 
              campeao["pontos"], campeao["construtora_campea"]))

    log.info("✅ Dados de Histórico de Campeões (1960-2024) inseridos com sucesso!")
    
    conn.commit()
    log.info("✅ Dados iniciais da F1 inseridos com sucesso!")